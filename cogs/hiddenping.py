import discord
import re
from discord.ext import commands
from discord.ext.commands import Bot
tos = ["nigga", "nigger", "nigguh"]


class hiddenping(commands.Cog):
    @commands.command()
    @commands.cooldown(1, 5, commands.BucketType.user)
    async def hiddenping(self, ctx, *args):
        name = args[0]
        user = re.sub('[^0-9]', '', args[1])
        await ctx.message.delete()
        for word in tos:
            if word in ctx.message.content:
                return await ctx.send("Absolutely not!\nAre you trying to get me banned?")
            else:
                return await ctx.send(f"@{name}||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​||||​|||| |||| || <@!{user}>")


def setup(bot):
    bot.add_cog(hiddenping(bot))



